# What is a method?
# A method is a grouped collection of Ruby statements that serves a purpose.
# Methods are designed to be reused, follow the golden rule of Don't Repeat Yourself (DRY)
# Methods abstract the complexity of solving a problem
# ex:

def who_am_i
    p "My name is Fog"
    p "I am smart"
    p "And love to code"
end

who_am_i
