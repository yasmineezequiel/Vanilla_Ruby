# Is also available .values and .keys to extract keys or values from a hash returning an Array

capitals = {Sweden: "Stockholm", Norway: "Oslo", Finland: "Helsinki", Denmark: "Copenhagen", Iceland: "Reykjavik"}

p capitals.values # ["Stockholm", "Oslo", "Helsinki", "Copenhagen", "Reykjavik"]
p capitals.keys # [:Sweden, :Norway, :Finland, :Denmark, :Iceland]
