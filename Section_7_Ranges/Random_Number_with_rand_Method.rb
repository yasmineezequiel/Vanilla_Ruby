# rand method it's similar with puts methods we don't need to call from an specific object:

puts rand # will generate a random number of 0.a_lot

puts rand.round(2) # generates 2 decimals after o like 0.85

puts rand(100) # numbers from 0-99

puts rand(101) # numbers from 0-100

puts rand(1..5) # numbers from 1 until 5

puts rand(1...5) # numbers from 1 until 4
